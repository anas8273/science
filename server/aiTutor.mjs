export function fallbackTutorAnswer(question) {
  const normalized = String(question || "").toLowerCase();
  
  for (const [, topic] of Object.entries(TOPIC_RESPONSES)) {
    if (topic.keywords.some(keyword => normalized.includes(keyword))) {
      return topic.answer;
    }
  }
  
  return `مرحباً! أنا **عالِمة**، مساعدتك في العلوم.

يمكنني مساعدتك في:
- 🔬 شرح المفاهيم العلمية (القوة، الخلية، الذرة...)
- 🧪 اقتراح تجارب آمنة
- 📝 وضع خطة مراجعة
- ❓ حل أسئلة الاختبارات

حددي المفهوم أو الموضوع الذي تريدين مساعدة فيه، وسأشرحه لكِ بطريقة واضحة مع أمثلة من حياتك اليومية.`;
}

function extractResponseText(data) {
  if (typeof data?.output_text === "string" && data.output_text.trim()) return data.output_text.trim();
  const text = data?.output
    ?.flatMap((item) => item?.content ?? [])
    ?.map((content) => content?.text)
    ?.filter(Boolean)
    ?.join("\n");
  if (text?.trim()) return text.trim();
  // Support for chat completions format
  const chatText = data?.choices?.[0]?.message?.content;
  if (chatText?.trim()) return chatText.trim();
  return "";
}

export async function handleTutorRequest(payload, env = process.env) {
  const question = String(payload?.question || "").trim();
  const context = payload?.context ?? {};
  if (!question) {
    return { status: 400, body: { error: "question_required", answer: "اكتبي سؤالك العلمي أولاً." } };
  }

  const apiKey = env.OPENAI_API_KEY;
  if (!apiKey) {
    return {
      status: 200,
      body: {
        provider: "fallback",
        answer: fallbackTutorAnswer(question),
      },
    };
  }

  const model = env.OPENAI_MODEL || "gpt-4.1-mini";
  
  const contextInfo = context.grade ? `\nالصف: ${context.grade === 'first' ? 'الأول متوسط' : 'الثالث متوسط'}` : '';
  const lessonInfo = context.currentLesson ? `\nالدرس الحالي: ${context.currentLesson}` : '';
  
  const input = [
    {
      role: "developer",
      content: SCIENCE_SYSTEM_PROMPT,
    },
    {
      role: "user",
      content: `السؤال: ${question}${contextInfo}${lessonInfo}

سياق إضافي:
${JSON.stringify(context, null, 2)}`,
    },
  ];

  try {
    const response = await fetch("https://api.openai.com/v1/responses", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${apiKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model,
        input,
        max_output_tokens: 800,
        temperature: 0.4,
        store: false,
      }),
    });

    if (!response.ok) {
      // Try chat completions as fallback
      const chatResponse = await fetch("https://api.openai.com/v1/chat/completions", {
        method: "POST",
        headers: {
          Authorization: `Bearer ${apiKey}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model,
          messages: [
            { role: "system", content: SCIENCE_SYSTEM_PROMPT },
            { role: "user", content: `السؤال: ${question}${contextInfo}${lessonInfo}` },
          ],
          max_tokens: 800,
          temperature: 0.4,
        }),
      });

      if (!chatResponse.ok) {
        return {
          status: 200,
          body: {
            provider: "fallback",
            warning: "api_unavailable",
            answer: fallbackTutorAnswer(question),
          },
        };
      }

      const chatData = await chatResponse.json();
      const chatAnswer = extractResponseText(chatData) || fallbackTutorAnswer(question);
      return {
        status: 200,
        body: {
          provider: "openai",
          answer: chatAnswer,
        },
      };
    }

    const data = await response.json();
    const answer = extractResponseText(data) || fallbackTutorAnswer(question);
    return {
      status: 200,
      body: {
        provider: "openai",
        answer,
      },
    };
  } catch (error) {
    return {
      status: 200,
      body: {
        provider: "fallback",
        warning: "network_error",
        answer: fallbackTutorAnswer(question),
      },
    };
  }
}
